<div class="underline"><h3 id="heading">History</h3></div>
<p>The <b>International Central Gospel Church-ICGC</b>  is an Evangelical, Charismatic Christian Church. It was officially inaugurated as a church on the 26th of February 1984, in Accra, Ghana. The first meeting was held in a small classroom with an initial membership of just about twenty people.</p>
</div>
