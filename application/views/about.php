<div>
    <div class="underline"  ><h3 id="heading">Our Mission Statement</h3></div>
<ul>
	<li>Raising Leaders</li>
	<li>Shaping Vision</li>
	<li>Influencing Society through Christ</li>
</ul>
<p>Through our programs, we trust to make the lives of our people better than before they came to Church,
and redirect their perception and behaviour in conformity with God's word. This attitude which is based on the word of God will transform the lives of people in their communities, work place, schools and so on.</p>
</div>
