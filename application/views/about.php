 	<div class="underline"><h3 id="heading">Vision of ICGC (Acts 2:42)</h3></div>
	<p>To establish the house of God, through the development of Model New Testament Christians and churches.</p>
	<p>That is, we have the commitment to train and equip God's people who come to our church to develop and grow into maturity in Christ so they will manifest the character of Christ.</p>
