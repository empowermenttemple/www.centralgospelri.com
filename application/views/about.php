    <div class="underline"><h3 id="heading">Our Message</h3></div>
<p>The I.C.G.C. is committed to the holistic preaching and demonstration of God's plans and purposes for all mankind as revealed in the Holy Scriptures. This plan is set out in Genesis 1:26-28.</p>

<p>Then God said, <i>Let Us make man in our image, according to our likeness; let them have dominion over the fish of the sea, over the birds of the air, and over the cattle, over all the earth and over every creeping thing that creeps on the earth.</i> So God created man in His own image; in the image of God He created him; male and female He created them.</p>

<p>Then God blessed them, and God said to them, <i>Be fruitful and multiply; fill the earth and subdue it; have dominion over the fish of the sea, over the birds of the air, and over every living thing that moves on the earth.</i> It is a purpose that was lost through the fall but restored through the conception, birth, life, ministry, death and resurrection of our Lord Jesus Christ. Through the gospel of His Kingdom, God has made available to mankind.</p>
